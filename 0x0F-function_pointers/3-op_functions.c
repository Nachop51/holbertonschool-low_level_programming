/**
 * op_add - adds two numbers
 * @a: first number
 * @b: second number
 *
 * Return: the sum
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - substract two numbers
 * @a: first number
 * @b: second number
 *
 * Return: the difference
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies two numbers
 * @a: first number
 * @b: second number
 *
 * Return: the multiplication
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides two numbers
 * @a: first number
 * @b: second number
 *
 * Return: the division
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - get the module of two numbers
 * @a: first number
 * @b: second number
 *
 * Return: the module
 */
int op_mod(int a, int b)
{
	return (a % b);
}
